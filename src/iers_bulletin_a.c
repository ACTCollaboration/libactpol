static const int mjd_min = 54466;
static const int mjd_max = 56717;

int
actpol_get_iers_bulletin_a( double mjd, double *dut1, double *x, double *y )
{
    if ( (mjd_min < mjd) && (mjd < mjd_max) )
    {
        double mjd_floor;
        double r = modf( mjd, &mjd_floor );
        int k = (int) mjd_floor - mjd_min;
        xyz a = bulletinA[k];
        xyz b = bulletinA[k+1];
        *dut1 = (1-r)*a.dut1 + r*b.dut1;
        *x = (1-r)*a.x + r*b.x;
        *y = (1-r)*a.y + r*b.y;
    }
    else
    {
        assert( 1 == 0 );
        return -1;
    }

    return 0;
}

